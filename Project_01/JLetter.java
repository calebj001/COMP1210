/**
* A program that displays a large, block letter J
* composed of the characters 'J', 'A', 'V', and 'A'.
* The width will be 12 characters and the height will be 10 lines.
*
* Project 1 - JLetter
* @author Caleb Johnson - COMP1210.
* @version 23 August, 2020.
*/
public class JLetter
{

   /**
   * Prints 'J', 'A', 'V', 'A' in the pattern of the letter J.
   * @param args Command line arguments - not used.
   */
   public static void main(String[] args)
   {
      
      // Print 2 lines of "JAVAJAVAJAVA".
      System.out.println("JAVAJAVAJAVA");
      System.out.println("JAVAJAVAJAVA");
      
      // Print 4 lines of "      JAVA".
      System.out.println("      JAVA");
      System.out.println("      JAVA");
      System.out.println("      JAVA");
      System.out.println("      JAVA");
   
      // Print 1 line of "J     JAVA".
      System.out.println("J     JAVA");
      
      // Print 1 line of "JA    JAVA".
      System.out.println("JA    JAVA");
      
      // Print 1 line of " JAVAJAVA".
      System.out.println(" JAVAJAVA");
      
      //Print 1 line of "  JAVAJA".
      System.out.println("  JAVAJA");
     
   }
   
}